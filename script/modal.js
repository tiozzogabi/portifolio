const Modal = {
    open(){
        //abrir modal
        var modal = document.querySelector('.modal-overlay');
        //adicional a class active do modal
        modal.classList.add('active');
    },
    close(){
        //fechar o modal
        //remover a class active do modal
        modal.classList.remove('active');

    }
}
